/**
 * 中国語テキストにピンインを付与してHTMLを生成する（高精度版）
 * - 複合語を優先的にマッチ（長い順）
 * - スペース区切りピンインで各文字に正確に対応
 * @param {string} text - 中国語テキスト
 * @returns {string} - ピンイン付きHTML文字列
 */
function addPinyinToText(text) {
  if (!text) return '';

  // 長さ順（降順）にソートしたキー一覧を作成
  const sortedKeys = Object.keys(CHAR_PINYIN).sort((a, b) => b.length - a.length);

  let html = '';
  let i = 0;

  while (i < text.length) {
    let matched = false;

    // 長い順にマッチを試みる
    for (const word of sortedKeys) {
      if (text.startsWith(word, i)) {
        const pinyin = CHAR_PINYIN[word];
        if (word.length === 1) {
          // 1文字
          html += `<span class="pinyin-block"><span class="pinyin-ruby">${pinyin}</span><span class="pinyin-char">${word}</span></span>`;
        } else {
          // 複合語：スペース区切りピンインを各文字に対応させる
          const pinyinParts = pinyin.split(' ');
          for (let j = 0; j < word.length; j++) {
            html += `<span class="pinyin-block"><span class="pinyin-ruby">${pinyinParts[j] || ''}</span><span class="pinyin-char">${word[j]}</span></span>`;
          }
        }
        i += word.length;
        matched = true;
        break;
      }
    }

    if (!matched) {
      const char = text[i];
      if (/[\u4e00-\u9fff\u3400-\u4dbf]/.test(char)) {
        // 辞書にない漢字
        html += `<span class="pinyin-block"><span class="pinyin-ruby">?</span><span class="pinyin-char">${char}</span></span>`;
      } else {
        // 漢字以外（句読点・スペース・英数字等）
        const escaped = char.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
        html += `<span class="pinyin-char pinyin-punct">${escaped}</span>`;
      }
      i++;
    }
  }

  return html;
}

/**
 * 中国語テキストから全ての漢字・単語を網羅的に抽出する
 * WORD_DICT → CHAR_PINYIN複合語 → 1文字漢字 の優先順で抽出
 * @param {string} chineseText
 * @returns {Array} 抽出された単語リスト
 */
function extractAllWordsFromText(chineseText) {
  if (!chineseText) return [];

  const results = [];
  const seen = new Set();

  // 1. WORD_DICT から検索（詳細情報あり）
  WORD_DICT.forEach(word => {
    if (chineseText.includes(word.zh) && !seen.has(word.zh)) {
      seen.add(word.zh);
      results.push({
        zh: word.zh,
        pinyin: word.pinyin,
        meaning: word.meaning,
        type: word.type,
        example: word.example || '',
        fromDict: true
      });
    }
  });

  // 2. CHAR_PINYIN の複合語（2文字以上）から検索
  const multiKeys = Object.keys(CHAR_PINYIN)
    .filter(k => k.length >= 2)
    .sort((a, b) => b.length - a.length);

  multiKeys.forEach(word => {
    if (chineseText.includes(word) && !seen.has(word)) {
      seen.add(word);
      results.push({
        zh: word,
        pinyin: CHAR_PINYIN[word].replace(/ /g, ''),
        meaning: '',
        type: '',
        example: '',
        fromDict: false
      });
    }
  });

  // 3. 1文字の漢字も抽出（CHAR_PINYIN に存在するもの）
  for (let i = 0; i < chineseText.length; i++) {
    const char = chineseText[i];
    if (/[\u4e00-\u9fff]/.test(char) && !seen.has(char) && CHAR_PINYIN[char]) {
      seen.add(char);
      results.push({
        zh: char,
        pinyin: CHAR_PINYIN[char],
        meaning: '',
        type: '',
        example: '',
        fromDict: false
      });
    }
  }

  return results;
}

// 後方互換のため旧関数名も維持
function extractWordsFromText(chineseText) {
  return extractAllWordsFromText(chineseText);
}
